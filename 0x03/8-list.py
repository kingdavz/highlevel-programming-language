#!/usr/bin/python
"""A module that can take the the first and last items on a list"""

fruits = ["Apple", "coconut", "Mango", "Orange", "Grapes"]

print("first item: ", fruits[0])
print("last item: ", fruits[-1])

num = [1, 2, 3, 4, 5, 6]

combined_list = fruits + num
print("Combined list: ", combined_list)