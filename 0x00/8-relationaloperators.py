#!/usr/bin/python
"""A module that performs relational operations"""
num1 = float(input("enter first number : "))
num2 = float(input("enter second number: "))
print(f"{num1} == {num2} --> {num1 == num2}")
print(f"{num1} != {num2} --> {num1 != num2}")
print(f"{num1} <  {num2} --> {num1 <  num2}")
print(f"{num1} >  {num2} --> {num1 >  num2}")
print(f"{num1} <= {num2} --> {num1 <= num2}")
print(f"{num1} >= {num2} --> {num1 >= num2}")



